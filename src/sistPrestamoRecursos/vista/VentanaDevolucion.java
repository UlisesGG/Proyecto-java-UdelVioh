    public void actualizarTabla(){
        String [][] data= ControladorPrestamos.getInstancia().listarPrestamosPendientes();
        if(data!=null){
            String [] colum={"Fecha de prestamo", "Devolucion pactada", "Devolucion real", "Nombre recurso", "RUT", "Amigo prestatario"};
            TableModel model=new DefaultTableModel(data, colum);
            jTable1.setModel(model);
        }else{
            JOptionPane.showMessageDialog(null, "No existen prestamos pendientes", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            Main ventana=new Main();
            ventana.setVisible(true);
            dispose();
        }
    }
