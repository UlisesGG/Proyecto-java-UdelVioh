    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        // TODO add your handling code here:
        int [] coso=tablita.getSelectedRows();
        if(coso.length>0){
            if(textCodigo.getText().length()>0 && textNombre.getText().length()>0 && textDescripcion.getText().length()>0){
                int cod=Integer.parseInt(textCodigo.getText());
                String nombre=textNombre.getText();
                String descripcion=textDescripcion.getText();
                int [] codigos=new int[coso.length];
                for(int i=0; i<coso.length; i++){
                    int codigo=Integer.parseInt((String) tablita.getValueAt(coso[i], 0));
                    codigos[i]=codigo;
                }
                ControladorRecursos insta=ControladorRecursos.getInstancia();
                insta.crearRecursoCompuesto(cod, nombre, descripcion, codigos);
            }else{
                JOptionPane.showMessageDialog(null, "No debe dejar ningun campo en blanco", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Debe elegir minimo un recurso para crear un recurso compuesto", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botonAgregarActionPerformed

