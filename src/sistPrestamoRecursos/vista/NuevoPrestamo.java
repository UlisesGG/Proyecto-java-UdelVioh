    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int duracion=(int) spinerCantidad.getValue();
        String tipo = (String) tipoDeDatos.getSelectedItem();
        try{
            int codigo=Integer.parseInt((String) tablaRecursos.getValueAt(tablaRecursos.getSelectedRow(), 0));
            String rut=(String) tablaAmigos.getValueAt(tablaAmigos.getSelectedRow(), 0);
            LocalDate fechaD=LocalDate.now();
            LocalDate fechaP;
            switch (tipo) {
                case "Dia(s)":
                    fechaDevolucion.setText(fechaD.plusDays(duracion).getDayOfMonth()+"/"+fechaD.plusDays(duracion).getMonthValue()+"/"+fechaD.plusDays(duracion).getYear());
                    fechaP=fechaD.plusDays(duracion);
                    break;
                case "Semana(s)":
                    fechaDevolucion.setText(fechaD.plusWeeks(duracion).getDayOfMonth()+"/"+fechaD.plusWeeks(duracion).getMonthValue()+"/"+fechaD.plusWeeks(duracion).getYear());
                    fechaP=fechaD.plusWeeks(duracion);
                    break;
                default:
                    fechaDevolucion.setText(fechaD.plusMonths(duracion).getDayOfMonth()+"/"+fechaD.plusMonths(duracion).getMonthValue()+"/"+fechaD.plusMonths(duracion).getYear());
                    fechaP=fechaD.plusMonths(duracion);
                    break;
            }
            ControladorPrestamos insta=ControladorPrestamos.getInstancia();
            insta.prestamos(LocalDate.now(), fechaP, codigo, rut);
            actualizarTabla();
        }catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "No hay seleccion de un recurso ni de un amigo, no se puede realizar el prestamo", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

