    public void actualizarTabla(){
        String [][] data=ControladorAmigos.getInstancia().listarAmigosSinPretamoPendiente();
        String [][] data2=ControladorRecursos.getInstancia().listarRecursosActivosNoPrestados();
        if(data!=null && data2!=null){
            String [] columName={"R.U.T.", "Nombre", "Correo", "Telefono"};
            String [] columName2={"Codigo", "Nombre", "Estado", "Valor", "Tipo"};
            TableModel model=new DefaultTableModel(data, columName);
            TableModel model2=new DefaultTableModel(data2, columName2);
            tablaAmigos.setModel(model);
            tablaRecursos.setModel(model2);
        }else{
            JOptionPane.showMessageDialog(null, "No existen amigos/recursos para realizar prestamos", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            Main ventana=new Main();
            ventana.setVisible(true);
            dispose();
        }
    }
